if __name__ == "__main__":
  print(f"4.2: {4.2}")
  print(f"type(4.2): {type(4.2)}")

  print(f"4.: {4.}")
  print(f"type(4.): {type(4.)}")

  print(f".2: {.2}")
  print(f"type(.2): {type(.2)}")

  print(f".4e7: {.4e7}")
  print(f"type(.4e7): {type(.4e7)}")

  print(f"4.2e-4: {4.2e-4}")
  print(f"type(4.2e-4): {type(4.2e-4)}")

  print(f"1.79e308: {1.79e308}")
  print(f"type(1.79e308): {type(1.79e308)}")

  print(f"1.8e308: {1.8e308}")              # value is inf
  print(f"type(1.8e308): {type(1.8e308)}")  # type is float

  print(f"5e-324: {5e-324}")
  print(f"type(5e-324): {type(5e-324)}")

  print(f"1e-325: {1e-325}")                # value is 0.0
  print(f"type(1e-325): {type(1e-325)}")    # type is float
