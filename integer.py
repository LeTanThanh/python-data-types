if __name__ == "__main__":
  dec = 10
  print(f"dec: {dec}")
  print(f"type(dec): {type(dec)}")

  bin = 0b10
  print(f"bin: {bin}")
  print(f"type(bin): {type(bin)}")

  oct = 0o10
  print(f"oct: {oct}")
  print(f"type(oct): {type(oct)}")

  hex = 0x10
  print(f"hex: {hex}")
  print(f"type(hex): {type(hex)}")
